int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	s;

	i = 0;
	s = 1;
	if (power < 0)
	{
		return (0);
	}
	else if (power == 0)
	{
		return (1);
	}
	else
	{
		while (i < power)
		{
			s = nb * s;
			i++;
		}
	}
	return (s);
}
