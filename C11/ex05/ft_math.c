#include <unistd.h>

int	ft_addition(int i1, int i2)
{
	return (i1 + i2);
}

int	ft_subtraction(int i1, int i2)
{
	return (i1 - i2);
}

int	ft_multiplication(int i1, int i2)
{
	return (i1 * i2);
}

int	ft_division(int i1, int i2)
{
	return (i1 / i2);
}

int	ft_modulo(int i1, int i2)
{
	return (i1 % i2);
}
